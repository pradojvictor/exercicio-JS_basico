//`Média entre números` | Crie uma variavel e atribua como valor uma operacao de média
// entre 5 números inteiros e positivos. |



function mediadeCinco(valor1,valor2,valor3,valor4,valor5) {
    return (valor1 + valor2 + valor3 + valor4 + valor5)/5
} 

console.log(mediadeCinco(5,5,5,5,5));
    
